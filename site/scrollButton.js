function changeKitty(mood) {
    var kittyImg = document.getElementById('kittyImg');
    if (mood === 'happy') {
      kittyImg.src = 'https://i.pinimg.com/originals/d0/d0/32/d0d032fd6e907316d33ebdd894752518.gif';
    } else if (mood === 'sad') {
      kittyImg.src = 'https://i.pinimg.com/originals/d0/d0/32/d0d032fd6e907316d33ebdd894752518.gif';
    }
  }
  